####   used to find length of any iterable object

"""
syntax
len(obj_name)

"""


# iterable object 

"""
string
range
list
tup set dict etc


"""


x = range(0,10 , 2)

print(len(x)) #=======> 5 


x = "adarsh"
print(len(x)) #=======> 6



x = [1,6,True , "adarsh"]
print(len(x)) #=======> 4





x = { 
    "fname" : "adarsh" ,
    "lname" : "dwivedi" ,
    "age" : 21

 }
print(len(x)) #=======> 3 

